//직사각형 별찍기

#include <stdio.h>

int main(void) {
    int a;
    int b;
    int i;
    int j;
    
    scanf("%d %d", &a, &b);
    i = 0;
    while(i != b)
    {
        j = 0;
        while (j != a)
        {
            printf("*");
            j++;
        }
        printf("\n");
        i++;
    }
    return 0;
}